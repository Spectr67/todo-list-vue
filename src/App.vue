<script>
import TodoInputComponent from './todoinputComponent.vue'

export default {
  components: { TodoInputComponent },
  data() {
    return {
      todoText: '',
      currentID: 0,
      todos: [],
    }
  },

  methods: {
    handleAddTask() {
      if (this.todoText === '') return
      this.currentID = this.currentID + 1
      const todo = {
        text: this.todoText,
        done: false,
        id: this.currentID,
      }
      this.todos.push(todo)
      this.todoText = ''
    },

    handleRemoveTaskById(id) {
      this.todos = this.todos.filter(todo => todo.id !== id)
    },
  },
}
</script>

<template>
  <div class="container">
    <h1>Список задач</h1>

    <!-- @input-change="handleInputChange" -->

    <TodoInputComponent
      :value="todoText"
      @handle-add-task="console.log($event)"
    ></TodoInputComponent>

    <ul id="taskList">
      <li
        v-for="todo of todos"
        :key="todo.id"
        :class="{ completed: todo.done }"
      >
        <input
          v-bind:checked="todo.done"
          v-on:change="todo.done = $event.target.checked"
          type="checkbox"
          class="checkbox"
        />

        <span class="task-text"> {{ todo.text }} </span>

        <button @click="handleRemoveTaskById(todo.id)" class="deleteButton">
          Удалить
        </button>
      </li>
    </ul>
  </div>
</template>

